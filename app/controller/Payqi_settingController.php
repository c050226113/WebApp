    private function isUser(){
        if($this->getUsername() && $this->getPassword()){
            return true;
        }else{
            return false;
        }
    }

    private function imeiChange($imei){
        $imei = substr($imei,0,14);

        $yanxinArr = $this->getYanxingImeiArr();

        if(count($this->getInput()) != count($yanxinArr))
            throw new Exception('数据不匹配');

        $has = false;
        foreach($this->getInput() as $k => $v){
            if( substr($v,0,14) == $imei){
                $has = true;
                break;
            }
        }
        if(!$has)
            throw new Exception('imei非法');

        $imei = substr($yanxinArr[$k],0,14);
        return $imei;
    }

    public function actionReset(){
        try{
            if(Request::isPost()){
                $password = '1234567';

                $imei = $this->imeiChange(Request::hasPost(self::CODE));
                if(!$imei)
                    throw new Exception('参数错误im');

                $tel =  Request::hasPost(self::ACCOUNT);
                if(!$tel)
                    throw new Exception('参数错误te');

                $role =  Request::hasPost(self::TYPE);
                if(!$role)
                    throw new Exception('参数错误ro');

                //发送邮件
                $url = 'http://120.24.180.7:8088/peiqi/web/index.php?r=devicemanager/sendemail';
                $data = [
                    'device' => $imei
                ];
                $res = HttpHelper::httpCurl($url, $data, $this->getCookie());
                $arr = @json_decode($res,1);
                if($arr['code'])
                    throw new Exception($arr['msg'].'!!!!');

                //进行登陆
                $res = $this->login($tel,$password);
                if(!is_array($res))
                    throw new Exception($res);

                //接触绑定
                $res = $this->unSub($tel, $password, $imei, $role);
                if(!is_array($res))
                    throw new Exception($res);

                e(json_encode(array("code" => 0)));
            }
        }catch (Exception $e){
            e(json_encode([self::CODE=>1,self::MESSAGE=>$e->getMessage()]));
        }
        q();
    }

    private function unSub($tel='', $password='', $imei='', $role=''){
        $c = $this->getSubscribeCol($imei);
        if(!$c)
            throw new Exception('参数错误c');

        //重置
        $url = "adminunsubscribe.php";
        $req_str = "u=".$tel."&up=".md5($password)."&s=".$imei."&r=".$role."&c=".$c."&p=".md5($password)."&tk=".HttpHelper::$token;
        $json = HttpHelper::curlPost(DB_SERVICE.$url,$req_str);//$json = '{"adminunsubscribed":[{"sn":"86742602300001","R":"2","C":"1","A":1}]}';
        $resArr = @json_decode($json,1);
        $arr = Util::hasArr($resArr,"adminunsubscribed");
        if($arr){
            return [true];
        }else{
            if($arr = Util::hasArr($resArr,"e")){
                return $arr[0];
            }
        }
    }

    private function login($tel='',$password=''){
        //先登陆
        $req_str = "u=".$tel."&up=".md5($password)."&p=2&t=".HttpHelper::$token;
        $url = "exlogin.php";
        $json = HttpHelper::curlPost(DB_SERVICE.$url,$req_str);

        $arr = json_decode($json,1);
        $data = @$arr["list"];

        if($data) {
            $this->setLoginData($data);
            return [true];
        }else{
            return '登陆失败';
        }
    }

    private $loginData = [];

    protected function getSubscribeCol($device){
        $data = $this->getLoginData();
        $data = $data[0];
        for($i=1;$i<=6;$i++){
            if($data["D".$i] == $device){
                return $i;
            }
        }
        return 1;
    }

    public function actionSearch(){
        try{
            if(Request::isPost()){
                $imei = $this->imeiChange(Request::hasPost(self::CODE));
                if(!$imei)
                    throw new Exception('参数错误im');

                $url = 'http://120.24.180.7:8088/peiqi/web/index.php?r=devicemanager/devices';
                $data = [
                    'device' => $imei
                ];
                $html = HttpHelper::httpCurl($url, $data, $this->getCookie());
                //匹配 </form> <style>
                preg_match('/<\/form>(.*?)<style>/uis',$html,$matched);

                if($matched[1]){
                    e(json_encode([self::CODE=>0,self::MESSAGE=> $matched[1]]));
                }else{
                    e(json_encode([self::CODE=>1,self::MESSAGE=>'系统错误','html'=>$html]));
                }
            }
        }catch (Exception $e){
            e(json_encode([self::CODE=>1,self::MESSAGE=>$e->getMessage()]));
        }
        q();
    }

